bool check_arrays(float *a, float *b, size_t length) {
    for (int i = 0; i < length; i++) {
        if (std::abs(a[i] - b[i]) >= 1E-4) {
            std::cerr << "\n====================\n";
            std::cerr << "Element " << i << " was not equal. Expected " << b[i] << " but got "
                      << a[i];
            std::cerr << "\n====================\n";
            return false;
        }
    }

    return true;
}

